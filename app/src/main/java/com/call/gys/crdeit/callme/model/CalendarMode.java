package com.call.gys.crdeit.callme.model;

public class CalendarMode {
    int Y;
    int M;
    int D;
    int W;

    public int getY() {
        return Y;
    }

    public void setY(int y) {
        Y = y;
    }

    public int getM() {
        return M;
    }

    public void setM(int m) {
        M = m;
    }

    public int getD() {
        return D;
    }

    public void setD(int d) {
        D = d;
    }

    public int getW() {
        return W;
    }

    public void setW(int w) {
        W = w;
    }
}
